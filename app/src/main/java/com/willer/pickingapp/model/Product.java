package com.willer.pickingapp.model;

public class Product {

    public Product() {
    }

    private String codigo;
    private String descripcion;
    private String codgrupo;
    private String codproveedor;
    private String unidades;
    private String codigobarras;
    private String referencia;
    private String peso;
    private String codigoexterno;
    private String ventascredito;
    private String permitedescuento;
    private String servicio;
    private String esflete;

    public String getCodigo() {
        return codigo;
    }

    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public String getCodgrupo() {
        return codgrupo;
    }

    public void setCodgrupo(String codgrupo) {
        this.codgrupo = codgrupo;
    }

    public String getCodproveedor() {
        return codproveedor;
    }

    public void setCodproveedor(String codproveedor) {
        this.codproveedor = codproveedor;
    }

    public String getUnidades() {
        return unidades;
    }

    public void setUnidades(String unidades) {
        this.unidades = unidades;
    }

    public String getCodigobarras() {
        return codigobarras;
    }

    public void setCodigobarras(String codigobarras) {
        this.codigobarras = codigobarras;
    }

    public String getReferencia() {
        return referencia;
    }

    public void setReferencia(String referencia) {
        this.referencia = referencia;
    }

    public String getPeso() {
        return peso;
    }

    public void setPeso(String peso) {
        this.peso = peso;
    }

    public String getCodigoexterno() {
        return codigoexterno;
    }

    public void setCodigoexterno(String codigoexterno) {
        this.codigoexterno = codigoexterno;
    }

    public String getVentascredito() {
        return ventascredito;
    }

    public void setVentascredito(String ventascredito) {
        this.ventascredito = ventascredito;
    }

    public String getPermitedescuento() {
        return permitedescuento;
    }

    public void setPermitedescuento(String permitedescuento) {
        this.permitedescuento = permitedescuento;
    }

    public String getServicio() {
        return servicio;
    }

    public void setServicio(String servicio) {
        this.servicio = servicio;
    }

    public String getEsflete() {
        return esflete;
    }

    public void setEsflete(String esflete) {
        this.esflete = esflete;
    }
}
